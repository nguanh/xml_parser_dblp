#Connect to mysql
cnx = mysql.connector.connect(user='root', password='master',
                              host='127.0.0.1')

#curso is used to pass SQL queries
cursor = cnx.cursor()

# try to change to DB  DB_NAME
try:
    cnx.database = DB_NAME
except mysql.connector.Error as err:
    #if there is no such db, create DB and change afterwards
    if err.errno == errorcode.ER_BAD_DB_ERROR:
        create_database(cursor, DB_NAME)
        cnx.database = DB_NAME
    else:
        print(err)
        exit(1)

#creates tables
for name, ddl in TABLES.items():
    #name =key = name of table
    #ddl = value= table creation query
    try:
        print("Creating table {}: ".format(name), end='')
        cursor.execute(ddl)
    except mysql.connector.Error as err:
        if err.errno == errorcode.ER_TABLE_EXISTS_ERROR:
            print("already exists.")
        else:
            print(err.msg)
    else:
        print("OK")

cursor.close()
cnx.close()